    /**
     * Builds verse ranges for Madinah 15-line layout.
     * For each page, determines the range of verse IDs that fall on that page.
     */
    public function run(): void
    {
        $this->command->info('Building page mappings...');

        // Build a lookup: [surah_id][verse_number] => global_verse_id
        $verseIdMap = $this->buildVerseIdMap();

        // Generate page boundaries for each layout
        $layouts = [
            'madinah_15' => 15,
            'madinah_13' => 13,
            'indonesia'  => 15,
            'pakistan'   => 13,
            'timteng_17' => 17,
        ];

        $totalVerses = 6236;
        $totalPages = 604;

        foreach ($layouts as $layoutCode => $linesPerPage) {
            $this->command->info("  Generating: {$layoutCode} ({$linesPerPage} lines/page)");

            if ($layoutCode === 'madinah_15' || $layoutCode === 'indonesia') {
                // 15-line: use authoritative page boundaries
                $boundaries = $this->getMadinah15Boundaries($verseIdMap);
            } else {
                // Other layouts: scale proportionally from Madinah 15
                $boundaries = $this->getScaledBoundaries(
                    $verseIdMap,
                    $linesPerPage,
                    15, // base lines per page
                );
            }

            // Generate (verse_id, layout_type, page_number) rows
            $rows = [];
            foreach ($boundaries as $pageNum => $verseIds) {
                foreach ($verseIds as $verseId) {
                    $rows[] = [
                        'verse_id'    => $verseId,
                        'layout_type' => $layoutCode,
                        'page_number' => $pageNum,
                        'created_at'  => now(),
                        'updated_at'  => now(),
                    ];
                }

                // Batch insert every 5000 rows
                if (count($rows) >= 5000) {
                    DB::table('page_mappings')->insertOrIgnore($rows);
                    $rows = [];
                }
            }

            if (!empty($rows)) {
                DB::table('page_mappings')->insertOrIgnore($rows);
            }
        }

        $this->command->info('Done! Page mappings seeded successfully.');
    }

    /**
     * Build a lookup: surah_id + verse_number → global verse_id.
     */
    private function buildVerseIdMap(): array
    {
        $map = [];
        $verseId = 1;

        foreach (QuranVerseSeeder::VERSE_COUNTS as $surahId => $count) {
            for ($v = 1; $v <= $count; $v++) {
                if (!isset($map[$surahId])) {
                    $map[$surahId] = [];
                }
                $map[$surahId][$v] = $verseId;
                $verseId++;
            }
        }

        return $map;
    }

    /**
     * Get authoritative page boundaries for 15-line Madinah Mushaf.
     * Returns: [page_number => [verse_id_1, verse_id_2, ...]]
     */
    private function getMadinah15Boundaries(array $verseIdMap): array
    {
        $boundaries = [];
        $pageStarts = self::MADINAH_15_PAGE_STARTS;

        // Recompute page starts to cover all 604 pages
        // The array above only covers some pages. We need all 604.
        // Let's interpolate between known boundaries.
        $allPageStarts = $this->interpolatePageStarts($pageStarts, $verseIdMap);

        for ($page = 1; $page <= 604; $page++) {
            $start = $allPageStarts[$page];

            if ($page < 604) {
                $end = $allPageStarts[$page + 1] - 1;
            } else {
                $end = 6236; // last verse
            }

            $verseIds = [];
            for ($vid = $start; $vid <= $end; $vid++) {
                $verseIds[] = $vid;
            }

            $boundaries[$page] = $verseIds;
        }

        return $boundaries;
    }

    /**
     * Interpolate all 604 page start verse IDs from known anchor points.
     */
    private function interpolatePageStarts(array $knownStarts, array $verseIdMap): array
    {
        // Convert known [surah, verse] starts to global verse IDs
        $knownPages = [];
        foreach ($knownStarts as $pageNum => $surahVerse) {
            [$surahId, $verseNum] = $surahVerse;
            $knownPages[$pageNum] = $verseIdMap[$surahId][$verseNum] ?? 1;
        }

        // Fix page 604 — it should be much later
        $knownPages[604] = 6236; // last verse

        // Sort by page number
        ksort($knownPages);

        $allStarts = [];
        $knownPageNumbers = array_keys($knownPages);

        for ($i = 0; $i < count($knownPageNumbers) - 1; $i++) {
            $startPage = $knownPageNumbers[$i];
            $endPage = $knownPageNumbers[$i + 1];
            $startVerse = $knownPages[$startPage];
            $endVerse = $knownPages[$endPage];

            $pageCount = $endPage - $startPage;
            $verseCount = $endVerse - $startVerse;

            for ($p = $startPage; $p < $endPage; $p++) {
                $fraction = ($p - $startPage) / $pageCount;
                $verseAtPage = (int) round($startVerse + $verseCount * $fraction);
                $allStarts[$p] = max($verseAtPage, 1);
            }
        }

        // Ensure page 1 starts at verse 1
        $allStarts[1] = 1;
        $allStarts[604] = 6236;

        // Fill any gaps
        for ($p = 1; $p <= 604; $p++) {
            if (!isset($allStarts[$p])) {
                $allStarts[$p] = $allStarts[$p - 1] ?? 1;
            }
        }

        return $allStarts;
    }

    /**
     * Scale Madinah 15 boundaries to other line counts.
     * More lines per page → more verses per page → fewer pages needed.
     * But we must keep exactly 604 pages, so we adjust distribution.
     */
    private function getScaledBoundaries(array $verseIdMap, int $targetLines, int $baseLines): array
    {
        // Get Madinah 15 boundaries first
        $madinahBoundaries = $this->getMadinah15Boundaries($verseIdMap);

        // Scale: ratio of verses per page
        $ratio = $targetLines / $baseLines;

        $scaled = [];
        $currentVerse = 1;
        $totalVerses = 6236;

        for ($page = 1; $page <= 604; $page++) {
            if ($page === 604) {
                // Last page gets all remaining verses
                $endVerse = $totalVerses;
            } else {
                // Estimate verses on this page based on ratio
                $baseCount = count($madinahBoundaries[$page]);
                $adjustedCount = (int) round($baseCount * $ratio);
                $adjustedCount = max($adjustedCount, 1); // at least 1 verse per page
                $endVerse = min($currentVerse + $adjustedCount - 1, $totalVerses);
            }

            $verseIds = [];
            for ($vid = $currentVerse; $vid <= $endVerse; $vid++) {
                $verseIds[] = $vid;
            }

            $scaled[$page] = $verseIds;
            $currentVerse = $endVerse + 1;

            if ($currentVerse > $totalVerses) {
                // Distribute remaining pages
                for ($remaining = $page + 1; $remaining <= 604; $remaining++) {
                    $scaled[$remaining] = [$totalVerses];
                }
                break;
            }
        }

        return $scaled;
    }
